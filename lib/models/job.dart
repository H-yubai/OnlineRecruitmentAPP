class Job {
  final String title;
  final String salary;
  final String company;
  final String companySize;
  final List<String> tags;
  final String hrName;
  final String location;

  Job({
    required this.title,
    required this.salary,
    required this.company,
    required this.companySize,
    required this.tags,
    required this.hrName,
    required this.location,
  });
}
